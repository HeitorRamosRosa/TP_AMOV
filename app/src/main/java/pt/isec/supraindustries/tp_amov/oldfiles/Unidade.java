package pt.isec.supraindustries.tp_amov.oldfiles;

public class Unidade {
    String nome;
    String abreviatura;

    public Unidade(String nome, String abreviatura) {
        this.nome = nome;
        this.abreviatura = abreviatura;
    }

    public String getNome() {
        return nome;
    }

    public String getAbreviatura() {
        return abreviatura;
    }
}
